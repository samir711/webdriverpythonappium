def add(x, y):
    """Add two numbers.
        Author : Samir
        Input : two numbers
        output : return sum of two numbers
      """
    return x + y


def sub(x, y):
    """Subtract two numbers.
        Author : Samir
        Input : two numbers
        output : returns Subtraction of two numbers .
      """
    return x - y


def mul(x, y):
    """Multiplication of two numbers.
        Author : Samir
        Input : two numbers
        output : returns Multiplication  of two numbers .
        """
    return x * y


def mod(x, y):
    """Modulus  of two numbers.
         Author : Samir
         Input : two numbers
         output :  returns Modulus  of two numbers .
          """
    return x % y


def div(x, y):
    """Division  of two numbers.
        Author : Samir
        Input : two numbers
        output :   returns division  of two numbers .
          """
    return x / y

