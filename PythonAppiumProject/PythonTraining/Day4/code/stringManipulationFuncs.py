# Range Slicing
str = "james bond"
z1 = str[2:]
print(z1)

# Slicing
z2 = str[0] + str[7]
print(z2)

# find if string is digit
print(str.isdigit())

# substring
str1 = 'I got you a cookie'
str2 = 'cook'
str3 = "paul"
# returns the start position of the substring
print(str1.find(str2))
print(str1.find(str3))

# replace
str1 = str.replace("a", "Z")
print(str1)
